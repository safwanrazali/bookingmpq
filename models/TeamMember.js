import mongoose from 'mongoose';

const { Schema, models, model } = mongoose;

const TeamMemberSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
      maxlength: 120,
    },
    email: {
      type: String,
      required: true,
      trim: true,
      lowercase: true,
    },
    phone: {
      type: String,
      trim: true,
      default: '',
    },
    position: {
      type: String,
      trim: true,
      default: '',
    },
    status: {
      type: String,
      enum: ['active', 'inactive'],
      default: 'active',
    },
  },
  { timestamps: true }
);

TeamMemberSchema.index({ status: 1 });

export default models.TeamMember || model('TeamMember', TeamMemberSchema);
